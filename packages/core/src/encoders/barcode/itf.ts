/**
 * ITF (Interleaved 2 of 5) 编码器
 */
export class ITFEncoder extends BarcodeEncoder {
  getType(): CodeType {
    return 'itf'
  }

  getMaxLength(): number {
    return 100
  }

  validate(content: string): boolean {
    return isValidITF(content) && content.length <= this.getMaxLength()
  }

  getModuleCount(content: string): number {
    const paddedLength = content.length % 2 === 0 ? content.length : content.length + 1
    const pairs = paddedLength / 2
    // 起始条(4) + 数据(pairs * 14) + 终止条(5)
    return 4 + pairs * 14 + 5
  }

  encodeToModules(content: string): number[] {
    // 确保偶数位
    const paddedContent = content.length % 2 === 0 ? content : `0${content}`

    const modules: number[] = []

    // 起始条：窄条-窄空-窄条-窄空
    modules.push(1, 1, 1, 1)

    // 编码数字对
    for (let i = 0; i < paddedContent.length; i += 2) {
      const pair = Number.parseInt(paddedContent.substring(i, i + 2), 10)
      const pattern = ITF_PATTERNS[pair]
      for (const width of pattern) {
        modules.push(width)
      }
    }

    // 终止条：宽条-窄空-窄条
    modules.push(3, 1, 1)

    return modules
  }
}
